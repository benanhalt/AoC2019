data Grid = Grid
  { cols :: Int
  , rows :: Int
  , chars :: V.Vector Char
  }

data Queue a = Queue Int [a] [a] deriving Show

checkf :: Queue a -> Queue a
checkf (Queue i [] r) = Queue i (reverse r) []
checkf q = q

snoc :: a -> Queue a -> Queue a
snoc x (Queue i f r) = checkf $ Queue (i+1) f (x:r)

isEmpty :: Queue a -> Bool
isEmpty (Queue _ f _) = null f

empty :: Queue a
empty = Queue 0 [] []

front :: Queue a -> a
front (Queue _ f r) = head f

rest :: Queue a -> Queue a
rest (Queue i (x:xs) r) = checkf $ Queue (i-1) xs r

size :: Queue a -> Int
size (Queue i _ _) = i


newtype Pos = Pos {unPos :: (Int, Int)} deriving (Eq, Show, Ord)

data State = State
  { pos :: [Pos]
  , which :: Int
  , keys :: S.Set Char
  , steps :: Int
  } deriving (Show, Eq, Ord)


main :: IO ()
main =
  let
    grid = parseGrid input
    allKeys = findKeys grid
    botPos = findBot grid
    s = (\w -> State {pos = botPos, which=w, keys = S.empty, steps=0}) <$> [0..3]
    q =  foldl (flip snoc) empty s
  in
    print $ search grid allKeys q (foldl' (flip rememberState) S.empty s)


parseGrid :: [[Char]] -> Grid
parseGrid cs =
  let
    rows = length cs
    cols = length $ head cs
  in
    Grid {rows = rows, cols = cols, chars = V.concat $ V.fromList <$> cs}

rememberState :: State -> S.Set ([Pos], S.Set Char, Int) -> S.Set ([Pos], S.Set Char, Int)
rememberState State {pos, keys, which} seen = S.insert (pos, keys, which) seen

search :: Grid -> S.Set Char -> Queue State -> S.Set ([Pos], S.Set Char, Int) -> Int
search grid allKeys q seen =
  let
    s@State {keys, pos, which, steps} = front q
    cs = filter (not . (`S.member` keys)) $ filter isLower $ maybeToList $ look grid (pos !! which)
    keys' = foldl' (flip S.insert) keys cs
    newStates =
      if null cs
      then do
        let Pos (x,y) = pos !! which
        pos' <- [(x-1, y), (x, y-1), (x+1, y), (x, y+1)]
        let pos'' = updateAt which (Pos pos') pos
        guard $ not $ S.member (pos'', keys', which) seen
        let s' = State {pos = pos'', keys=keys', steps=steps+1, which}
        guard $ isGood grid s'
        pure s'
      else do
        which' <- [0..3]
        pure s {keys = keys', which = which'}
    q' = foldl' (flip snoc) (rest q) newStates
    seen' = foldl' (flip rememberState) seen newStates
  in
    (if (S.size seen') `mod` 10000 == 0 then traceShow (S.size seen', s) else id) $
    if keys' == allKeys then steps
    else search grid allKeys q' seen'

updateAt :: Int -> a -> [a] -> [a]
updateAt i a as = h ++ (a : (drop 1 t))
  where (h, t) = splitAt i as

isGood :: Grid -> State -> Bool
isGood grid State {keys, pos} =
  let
    isG pos = case look grid pos of
      Nothing -> False
      Just '#' -> False
      Just '.' -> True
      Just '@' -> True
      Just c -> isLower c || (isUpper c && toLower c `S.member` keys)
  in
    and $ isG <$> pos

findKeys :: Grid -> S.Set Char
findKeys grid@Grid {rows, cols, chars} = S.fromList $ do
  y <- [0 .. rows-1]
  x <- [0 .. cols-1]
  c <- maybeToList $ look grid (Pos (x,y))
  guard $ isLower c
  pure $ c

findBot :: Grid -> [Pos]
findBot grid@Grid {rows, cols} = do
  y <- [0 .. (rows-1)]
  x <- [0 .. (cols-1)]
  guard $ look grid (Pos (x,y)) == Just '@'
  pure $ Pos (x, y)

look :: Grid -> Pos -> Maybe Char
look Grid {rows, cols, chars} (Pos (x,y))
  | y >= rows = Nothing
  | y < 0 = Nothing
  | x >= cols = Nothing
  | x < 0 = Nothing
  | otherwise = Just $ chars V.! (x + y*cols)


